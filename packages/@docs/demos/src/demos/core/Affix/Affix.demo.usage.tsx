import { IconArrowUp } from '@tabler/icons-react';
import { Affix, Button, rem, Text, Transition } from '@nex-ui/core';
import { useWindowScroll } from '@nex-ui/hooks';
import { MantineDemo } from '@nex-uix/demo';

const code = `
import { IconArrowUp } from '@tabler/icons-react';
import { useWindowScroll } from '@nex-ui/hooks';
import { Affix, Button, Text, Transition, rem } from '@nex-ui/core';

function Demo() {
  const [scroll, scrollTo] = useWindowScroll();

  return (
    <>
      <Text ta="center">Affix is located at the bottom of the screen, scroll to see it</Text>
      <Affix position={{ bottom: 20, right: 20 }}>
        <Transition transition="slide-up" mounted={scroll.y > 0}>
          {(transitionStyles) => (
            <Button
              leftSection={<IconArrowUp style={{ width: rem(16), height: rem(16) }} />}
              style={transitionStyles}
              onClick={() => scrollTo({ y: 0 })}
            >
              Scroll to top
            </Button>
          )}
        </Transition>
      </Affix>
    </>
  );
}
`;

